.section .data
; ; score_digit_1: .space 3
score_digit_1: .byte 0x00
score_digit_2: .byte 0x00
score_digit_3: .byte 0x00

.section .text
.global print_score

print_score:
    lds  R23, score_digit_1
    subi R23, (-W)
    cpi R23, (W *10)
    brlo store+2            
    ldi R23, 0
    lds R24, score_digit_2
    subi R24, (-W)
    cpi R24, (W *10)
    brlo store
    ldi R24, 0
    lds R25, score_digit_3
    subi R25, (-W)
    sts score_digit_3, R25
store:
    sts score_digit_2, R24
    sts score_digit_1, R23    

    ; Print Digits

    ; TWI Start
    rcall start
    ldi R24, 0x78
    rcall Tx
    ldi R24, 0x40
    rcall Tx

    lds R24, score_digit_3
    rcall print_digit
    lds R24, score_digit_2
    rcall print_digit
    lds R24, score_digit_1
    rcall print_digit
    
    ; TWI Stop
    rcall stop



print_digit:
    ldi R25, 0x00
    ldi ZH, hi8(font_digit)
    ldi ZL, lo8(font_digit)
    add ZL, R24
    adc ZH, R25
    ldi R17, W-1  ; counter same as digit width
    ld R24, Z+
    rcall Tx
    dec R17
    brpl .-8
    ldi R24, 0
    rcall Tx
ret




